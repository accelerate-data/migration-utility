/

MERGE INTO "__SCHEMA__"."SILVER_CONFIG" tgt
USING (
    SELECT 1 AS config_id, 'full_reload' AS config_key, '1' AS config_value FROM dual
    UNION ALL
    SELECT 2 AS config_id, 'default_category' AS config_key, 'General' AS config_value FROM dual
    UNION ALL
    SELECT 3 AS config_id, 'cleanup_threshold' AS config_key, '1000' AS config_value FROM dual
) src
ON (tgt."CONFIGKEY" = src.config_key)
WHEN MATCHED THEN
    UPDATE SET
        tgt."CONFIGID" = src.config_id,
        tgt."CONFIGVALUE" = src.config_value
WHEN NOT MATCHED THEN
    INSERT ("CONFIGID", "CONFIGKEY", "CONFIGVALUE")
    VALUES (src.config_id, src.config_key, src.config_value);

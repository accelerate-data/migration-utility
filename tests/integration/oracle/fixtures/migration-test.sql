/

MERGE INTO "__SCHEMA__"."BRONZE_CURRENCY" tgt
USING (
    SELECT 'USD' AS currency_code, 'US Dollar' AS currency_name FROM dual
    UNION ALL
    SELECT 'EUR' AS currency_code, 'Euro' AS currency_name FROM dual
    UNION ALL
    SELECT 'GBP' AS currency_code, 'Pound Sterling' AS currency_name FROM dual
) src
ON (tgt."CURRENCYCODE" = src.currency_code)
WHEN MATCHED THEN
    UPDATE SET
        tgt."CURRENCYNAME" = src.currency_name,
        tgt."MODIFIEDDATE" = SYSDATE
WHEN NOT MATCHED THEN
    INSERT ("CURRENCYCODE", "CURRENCYNAME", "MODIFIEDDATE")
    VALUES (src.currency_code, src.currency_name, SYSDATE);
/

MERGE INTO "__SCHEMA__"."BRONZE_PROMOTION" tgt
USING (
    SELECT
        1 AS promotion_id,
        'Spring Sale' AS description,
        10 AS discount_pct,

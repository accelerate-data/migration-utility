CREATE OR REPLACE PROCEDURE "__SCHEMA__"."SILVER_USP_UNIONALL" AS
BEGIN
    DELETE FROM "__SCHEMA__"."SILVER_CONFIG"
    WHERE "CONFIGKEY" LIKE 'currency_%';

    INSERT INTO "__SCHEMA__"."SILVER_CONFIG" ("CONFIGKEY", "CONFIGVALUE")
    SELECT 'currency_' || LOWER("CURRENCYCODE"), "CURRENCYNAME"
    FROM "__SCHEMA__"."BRONZE_CURRENCY"
    WHERE "CURRENCYCODE" <= 'M'
    UNION ALL
    SELECT 'currency_' || LOWER("CURRENCYCODE"), "CURRENCYNAME"
    FROM "__SCHEMA__"."BRONZE_CURRENCY"
    WHERE "CURRENCYCODE" > 'M';
END "SILVER_USP_UNIONALL";
/


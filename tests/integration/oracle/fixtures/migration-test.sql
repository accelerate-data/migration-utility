/

CREATE TABLE "__SCHEMA__"."SILVER_CONFIG" (
    "CONFIGID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    "CONFIGKEY" VARCHAR2(100) NOT NULL,
    "CONFIGVALUE" VARCHAR2(255),
    CONSTRAINT "__SCHEMA___SILVER_CONFIG_PK" PRIMARY KEY ("CONFIGID"),

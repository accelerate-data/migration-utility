    "MINQTY" NUMBER,
    "MAXQTY" NUMBER,
    "MODIFIEDDATE" DATE NOT NULL,
    CONSTRAINT "__SCHEMA___BRONZE_PROMOTION_PK" PRIMARY KEY ("PROMOTIONID")

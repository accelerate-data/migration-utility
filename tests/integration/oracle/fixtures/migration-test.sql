CREATE OR REPLACE PROCEDURE "__SCHEMA__"."SILVER_USP_LOAD_DIMCURRENCY" AS
    v_sql CLOB;
BEGIN
    DELETE FROM "__SCHEMA__"."SILVER_DIMCURRENCY";

    v_sql := q'[
        INSERT INTO "__SCHEMA__"."SILVER_DIMCURRENCY" ("CURRENCYALTERNATEKEY", "CURRENCYNAME")
        SELECT "CURRENCYCODE", "CURRENCYNAME"
        FROM "__SCHEMA__"."BRONZE_CURRENCY"
    ]';

    EXECUTE IMMEDIATE v_sql;
END "SILVER_USP_LOAD_DIMCURRENCY";
/

